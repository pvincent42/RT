#include <libft.h>
#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	char		*dest;
	int			i;

	if (!src)
		return (NULL);
	if (!(dest = (char *)malloc(sizeof(char) * ft_strlen(src) + 1)))
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
