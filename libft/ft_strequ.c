int		ft_strequ(char const *s1, char const *s2)
{
	int		equ;

	if (!s1 || !s2)
		return (0);
	equ = 1;
	while (*s1 || *s2)
	{
		if (!(*s1 == *s2))
			equ = 0;
		s1++;
		s2++;
	}
	return (equ);
}
