#include <libft.h>

char	*ft_itoa(int n)
{
	char	*res;
	int		v[3];

	if (n == -2147483648 && (res = ft_memalloc(12)))
		return ((res = ft_strcpy(res, "-2147483648")));
	v[0] = ((n == 0) ? 1 : 0);
	v[1] = '0';
	v[2] = n;
	if (n < 0 && (v[1] = '-'))
	{
		n = -n;
		v[0]++;
	}
	while (v[2] && ++v[0])
		v[2] /= 10;
	res = ft_strnew(v[0]);
	if (n == 0 && (res[0] = '0'))
		v[0]++;
	res[v[0]] = '\0';
	res[0] = v[1];
	while (n && (res[(v[0]--) - 1] = n % 10 + '0'))
		n /= 10;
	return (res);
}
