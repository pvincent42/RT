#include <stddef.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		equ;

	if (!s1 || !s2)
		return (0);
	equ = 1;
	while (n--)
	{
		if (!(*s1 == *s2))
			equ = 0;
		s1++;
		s2++;
	}
	return (equ);
}
