#include <parser.h>

void			ft_get_ambiant_color(t_scene *s, t_object *o)
{
	o->ac[0] = s->ac[0] * o->ka * o->dc[0];
	o->ac[1] = s->ac[1] * o->ka * o->dc[1];
	o->ac[2] = s->ac[2] * o->ka * o->dc[2];
}
