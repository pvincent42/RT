#include <rt.h>

void				ft_modulo_key(int *n, const int max, const int dir)
{
	if (!dir)
		*n = (*n - 1) < 0 ? max - 1 : *n - 1;
	if (dir)
		*n = ft_abs((*n + 1) % max);
}
