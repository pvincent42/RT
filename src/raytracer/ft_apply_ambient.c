#include <rt.h>

void	ft_apply_ambient(t_ldata *d, t_object *o)
{
	d->cc = -1;
	while (++d->cc < 3)
	{
		d->c[d->cc] = o->ac[d->cc];
		ft_check_light(&d->c[d->cc]);
	}
}
