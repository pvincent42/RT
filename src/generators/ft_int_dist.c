#include <math.h>
#include <rt.h>

int					ft_int_dist(t_ci p1, t_ci p2)
{
	int				dist;

	dist = sqrt(pow(p1.x - p2.x, 2)
				+ pow(p1.y - p2.y, 2)
				+ pow(p1.z - p2.z, 2));
	return (dist);
}
